  public boolean validates()
  {
      if(txtentryid.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter ID");
          txtentryid.grabFocus();
          return false;
      }
      if(txtpname.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Name");
          txtpname.grabFocus();
          return false;
      }
      if(txtadhaarno.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Adhaar No");
          txtadhaarno.grabFocus();
          return false;
      }
      if(txtintime.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter In Time");
          txtintime.grabFocus();
          return false;
      }
      if(txtouttime.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Out Time");
          txtouttime.grabFocus();
          return false;
      }
      if(txtpersontovisit.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Person to visit");
          txtpersontovisit.grabFocus();
          return false;
      }
      if(txtpurpose.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter purpose");
          txtpurpose.grabFocus();
          return false;
      }
      if(txtvisitdate.getText().trim().isEmpty())
      {
          JOptionPane.showMessageDialog(addForm.this, "Enter Visit Date");
          txtvisitdate.grabFocus();
          return false;
      }
      return true;
  }
