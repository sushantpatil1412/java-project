    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(validates())
        {try {
              String sqlstr ="Insert into entrym values('"+ txtentryid.getText()+"','" + txtpname.getText()+"',"+ txtadhaarno.getText()+",'" + txtintime.getText()+"','" + txtouttime.getText()+"','" + txtpersontovisit.getText()+"','" + txtpurpose.getText()+"','"+txtvisitdate.getText()+"')";
              //System.out.println(sqlstr);
              st = (PreparedStatement) conn.prepareStatement(sqlstr);
              
            
            if(st.execute(sqlstr)== false)
            {
                JOptionPane.showMessageDialog(addForm.this, "Data inserted Successfully!!");
                this.setVisible(false);
                new MainUI().setVisible(true);
                conn.close();
            }
            else
            {
                JOptionPane.showMessageDialog(addForm.this, "Enter correct Data");
                
            }
         
        }
        catch (SQLException sqlException) {
                    sqlException.printStackTrace();
                }
        
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

